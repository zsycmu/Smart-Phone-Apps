package DBLayout;

public class Contact {
	String name;
	int phone;
	String email;
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	public int getPhone()
	{
		return phone;
	}
	
	public void setPhone(int phone)
	{
		this.phone = phone;
	}
	public String getEmail()
	{
		return name;
	}
	
	public void setEmail(String email)
	{
		this.email = email;
	}

}
